from odoo import fields, models


class PosConfig(models.Model):
    """To add a fields in pos config to view the positions of the screen"""
    _inherit = 'pos.config'

    screen_view = fields.Selection([('left', 'Left'), ('right', 'Right')],
                                   string="Screen View",
                                   help="To set the position of the screen",
                                   default='left', required=True)
